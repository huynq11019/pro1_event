    void loadKM() {
        dftable = (DefaultTableModel) tba.getModel();
        dftable.setRowCount(0);
        for (KhachMoi khachMoi : lsKhACH) {
            dftable.addRow(new Object[]{
                khachMoi.getTenKM(), khachMoi.getEmail(), khachMoi.getSdt(), khachMoi.getGhiChu()
            });
        }
        tba.setRowSelectionInterval(0, 0);
    }

    void loadCBsukien() {
        List<SuKien> lsSukien = skDAo.selectall();
        dfSukien = (DefaultComboBoxModel) dfsukien.getModel();
        dfSukien.removeAllElements();
        for (SuKien suKien : lsSukien) {
            dfSukien.addElement(suKien);
        }
    }


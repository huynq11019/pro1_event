    KhachmoiTD getKMTD(int row) {
        KhachmoiTD km = new KhachmoiTD();
        SuKien sk = (SuKien) dfSukien.getSelectedItem();
        km.setIdKM(lsKhACH.get(row).getIdKhachMoi());
        km.setIdSK(sk.getIDSK());
        return km;
    }

